public class NotWEBTestCaseException extends Exception {

	public NotWEBTestCaseException() {
		super();
	}

	public NotWEBTestCaseException(String message) {
		super(message);
	}

	public NotWEBTestCaseException(String message, Throwable cause) {
		super(message, cause);
	}

	public NotWEBTestCaseException(Throwable cause) {
		super(cause);
	}

}
